package lesson6;

public class MethodOverLoading1 {
	void show (int a) {
		System.out.println("Data type is int:" + a);
	}
	void show (double a) {
		System.out.println("Data type is double:" + a);
	}
	void show (String a) {
		System.out.println("Data type is String:" + a);
	}
	void show (boolean a) {
		System.out.println("Data type is boolean:" + a);
	}

}
